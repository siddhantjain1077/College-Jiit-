#Arithematic Operator
a = 10
b = 5

print("Addition:", a + b)

print("Subtraction:", a - b)
print("Multiplication:", a * b)

print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)

print("Exponentiation:", a ** b)

#Comparison Operator:
x = 67
y = 23
print("Not Equal:", x != y)
print("Greater Than:", x > y)
print("Less Than:", x < y)
print("Greater Than or Equal:", x >= y)

#logical Operat

p = True
q = False
print("AND:", p and q)
print("OR", q or p)